// Always define a package - an executable must have a 'main'
package main

// Import any external packages (from standard library - https://golang.org/pkg/ - or custom)
import "fmt"

// An executable must also have a function 'main()'
func main() {
	fmt.Println("Hello")
}

// Run with:
// > go run 1-hello-world.go

// Or
// > go build 1-hello-world.go
// > 1-hello-world.exe
